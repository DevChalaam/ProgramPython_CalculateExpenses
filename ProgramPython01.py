# โปรแกรมคำนวนค่าใช้จ่าย
money = float(input("กรอกรค่าใช้จ่ายทั้งหมด >>> "))
member = int(input("กรอกจำนวนสมาชิก >>> "))

if member > 10 :
    sales = 5 / 100 * money
    mixmoney = money - sales
    print("คุณมีจำนวนสมาชิกเกิน 10 คน คุณได้รับส่วนลด 5%", "คุณได้ส่วนลดเป็นเงิน", sales, "เงินที่คุณต้องชำระ เป็นเงิน", mixmoney)
else :
    print("คุณมีสมาชิกน้อยกว่า 10 คน คุณจึงไม่ได้รับส่วนลด")